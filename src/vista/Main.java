    private void btnConvertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConvertirActionPerformed
        txtInformacion.setText("");
        String monedaBase = cbxMonedaBase.getSelectedItem().toString();
        String monedaDestino = cbxMonedaDestino.getSelectedItem().toString();
        if (monedaBase != monedaDestino) {
            try {
                double cantidad = Double.parseDouble(txtCantidad.getText());
                
                Moneda moneda = consulta.convertir(monedas.get(monedaBase), monedas.get(monedaDestino), cantidad);
                txtResultado.setText(String.valueOf(moneda.conversion_result()));
                
                txtInformacion.setText("1 " + monedaBase + " es igual a " + moneda.conversion_rate() + " " + monedaDestino + " en este momento.");
            } catch (NumberFormatException e) {
                mensajeError("Ingrese valores num√©ricos");
            } catch (RuntimeException e) {
                mensajeError(e.getMessage());
                System.out.println(e.getMessage());
            }
        } else {
            mensajeError("Seleccione tipos de moneda diferente");
        }

    }//GEN-LAST:event_btnConvertirActionPerformed

